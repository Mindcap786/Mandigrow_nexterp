for (const [lang, data] of Object.entries(translations)) {
  const dirPath = path.join(localesDir, lang);
  if (!fs.existsSync(dirPath)) {
    fs.mkdirSync(dirPath, { recursive: true });
  }
  
  const targetFile = path.join(dirPath, 'common.json');
  
  // If english, merge with existing if there's any
  if (lang === 'en' && fs.existsSync(targetFile)) {
    const existing = JSON.parse(fs.readFileSync(targetFile, 'utf8'));
    // deep merge simple implementation
    const merge = (target, source) => {
      for (const key of Object.keys(source)) {
        if (source[key] instanceof Object && key in target) {
          Object.assign(source[key], merge(target[key], source[key]))
        }
      }
      Object.assign(target || {}, source)
      return target
    }
    const mergedData = merge(existing, data);
    fs.writeFileSync(targetFile, JSON.stringify(mergedData, null, 2), 'utf8');
  } else {
    fs.writeFileSync(targetFile, JSON.stringify(data, null, 2), 'utf8');
  }
  
  console.log(`Generated locales for ${lang}`);
}
